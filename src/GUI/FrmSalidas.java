    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtId.getText().isEmpty()) {

            Salida salida = new Salida();
            try {
                String pattern = "yyyy-MM-dd";
                SimpleDateFormat simpleDateFormat = new SimpleDateFormat(pattern);
                String date = simpleDateFormat.format(dtpFecha.getDate());
                Fecha fechaHora = new Fecha(date);

                pattern = "k";
                simpleDateFormat = new SimpleDateFormat(pattern);
                date = simpleDateFormat.format((Date) spinnerHora.getValue());
                fechaHora.setHora(Integer.valueOf(date));

                pattern = "m";
                simpleDateFormat = new SimpleDateFormat(pattern);
                date = simpleDateFormat.format((Date) spinnerHora.getValue());
                fechaHora.setMinuto(Integer.valueOf(date));

                salida.setFechaHora(fechaHora);
                salida.setBarco((Barco) cbxBarcos.getSelectedItem());
                salida.setDestino((Destino) cbxDestinos.getSelectedItem());
                salidaDao.add(salida);
                updateTable();
                limpiarCampos();
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }

        } else {
            if (salidaEdit != null) {
                try {
                    String pattern = "yyyy-MM-dd";
                    SimpleDateFormat simpleDateFormat = new SimpleDateFormat(pattern);
                    String date = simpleDateFormat.format(dtpFecha.getDate());
                    Fecha fechaHora = new Fecha(date);

                    pattern = "k";
                    simpleDateFormat = new SimpleDateFormat(pattern);
                    date = simpleDateFormat.format((Date) spinnerHora.getValue());
                    fechaHora.setHora(Integer.valueOf(date));

                    pattern = "m";
                    simpleDateFormat = new SimpleDateFormat(pattern);
                    date = simpleDateFormat.format((Date) spinnerHora.getValue());
                    fechaHora.setMinuto(Integer.valueOf(date));

                    salidaEdit.setFechaHora(fechaHora);
                    salidaEdit.setBarco((Barco) cbxBarcos.getSelectedItem());
                    salidaEdit.setDestino((Destino) cbxDestinos.getSelectedItem());
                    salidaDao.update(salidaEdit);
                    updateTable();
                } catch (Exception ex) {

                }
            }

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

