    private void tblSalidasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSalidasMouseClicked
        int row = tblSalidas.rowAtPoint(evt.getPoint());

        salidaEdit = lstSalidas.get(lstSalidas.indexOf(new Salida((Integer) tblSalidas.getValueAt(row, 0))));

        txtId.setText(String.valueOf(salidaEdit.getId()));
        dtpFecha.setCalendar(salidaEdit.getFechaHora());
        spinnerHora.setValue(salidaEdit.getFechaHora().getTime());
        cbxBarcos.setSelectedItem(salidaEdit.getBarco());
        cbxDestinos.setSelectedItem(salidaEdit.getDestino());
        btnGuardar.setText("Actualizar");

    }//GEN-LAST:event_tblSalidasMouseClicked

